  /// الحصول على جميع أسماء المراتب
  static List<String> get mattressNames =>
      mattressData.map((m) => m.name).toList();

  /// الحصول على جميع المقاسات الفريدة
  static List<String> get allSizes {
    final sizes = <String>{};
    for (final mattress in mattressData) {
      sizes.addAll(mattress.sizes);
    }
    // ترتيب المقاسات
    final sortedSizes = sizes.toList()
      ..sort((a, b) {
        final aParts = a.split('/');
        final bParts = b.split('/');
        final aLength = int.parse(aParts[0]);
        final bLength = int.parse(bParts[0]);
        if (aLength != bLength) return aLength.compareTo(bLength);
        final aWidth = int.parse(aParts[1]);
        final bWidth = int.parse(bParts[1]);
        return aWidth.compareTo(bWidth);
      });
    return sortedSizes;
  }

  /// الحصول على مقاسات العرض فقط (80, 90, 100, ...)
  static List<int> get widthSizes {
    final widths = <int>{};
    for (final size in allSizes) {
      final width = int.parse(size.split('/')[1]);
      widths.add(width);
    }
    return widths.toList()..sort();
  }

  /// الحصول على مقاسات الطول فقط (190, 200)
  static List<int> get lengthSizes {
    final lengths = <int>{};
    for (final size in allSizes) {
      final length = int.parse(size.split('/')[0]);
      lengths.add(length);
    }
    return lengths.toList()..sort();
  }

  /// البحث عن مرتبة بالاسم
  static MattressModel? findByName(String name) {
    try {
      return mattressData.firstWhere(
        (m) => m.name.toLowerCase() == name.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }
